package com.dcc.ProjectManagementSystem.entity;

public class Other {
	private Integer oid;
	private String other_name;
	private Integer other_tag;
	private String other_color;
	private String other_back_color;
	public Integer getOid() {
		return oid;
	}
	public void setOid(Integer oid) {
		this.oid = oid;
	}
	public String getOther_name() {
		return other_name;
	}
	public void setOther_name(String other_name) {
		this.other_name = other_name;
	}
	public Integer getOther_tag() {
		return other_tag;
	}
	public void setOther_tag(Integer other_tag) {
		this.other_tag = other_tag;
	}
	public String getOther_color() {
		return other_color;
	}
	public void setOther_color(String other_color) {
		this.other_color = other_color;
	}
	public String getOther_back_color() {
		return other_back_color;
	}
	public void setOther_back_color(String other_back_color) {
		this.other_back_color = other_back_color;
	}
}
